    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        int fila = tablaClientes.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Es necesario seleccionar un renglón");
        }
        else {
           operacion = "modifica";
           id =(String)tabla.getValueAt(fila, 0);
           nombreTxt.setText((String)tabla.getValueAt(fila, 1));
           direccionTxt.setText((String)tabla.getValueAt(fila, 2));
           telefonoTxt.setText((String)tabla.getValueAt(fila, 3));
           correoTxt.setText((String)tabla.getValueAt(fila, 4));
           nombreTxt.setEnabled(true);
           correoTxt.setEnabled(true);
           direccionTxt.setEnabled(true);
           telefonoTxt.setEnabled(true);
           Guardar.setEnabled(true);
           Cancelar.setEnabled(true);
           Borrar.setEnabled(true);
           Nuevo.setEnabled(false);
        }
    }//GEN-LAST:event_tablaClientesMouseClicked

