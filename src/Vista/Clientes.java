    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (operacion.equals("nuevo")){
           contol.registraCliente(nombreTxt.getText(), direccionTxt.getText(), telefonoTxt.getText(), correoTxt.getText());
           nombreTxt.setText("");
           direccionTxt.setText("");
           telefonoTxt.setText("");
           correoTxt.setText("");
        }
        else{
            if (operacion.equals("modifica")){
                contol.corrigeCliente(Integer.valueOf(id),nombreTxt.getText(), direccionTxt.getText(), telefonoTxt.getText(), correoTxt.getText());
            }
            cargaTabla();
        }
        cargaTabla();
}//GEN-LAST:event_GuardarActionPerformed

