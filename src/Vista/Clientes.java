    private void cargaTabla(){
        String [] titulos = {"Id", "Nombre", "Direccion","Telefono","Correo"};
        String [] registro = new String [5];
        Cliente cliente;
        ArrayList clientes;
        tabla = new DefaultTableModel(null,titulos);
        clientes = contol.listaClientes ();
        for (int i = 0; i<clientes.size(); i++)
        {
            cliente = (Cliente) clientes.get(i);
            registro [0] = Integer.toString (cliente.getNum_cliente());
            registro [1] = cliente.getNom_cliente ();
            registro [2] = cliente.getDirec_cliente ();
            registro [3] = cliente.getTel_cliente ();
            registro [4] = cliente.getImbox_cliente ();
            tabla.addRow (registro);
        }
        tablaClientes.setModel(tabla);
        tablaClientes.setEnabled(true);
    }

