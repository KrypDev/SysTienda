    private void cargaTabla (){
        String [] titulos = {"Id", "Supervisor", "Correo","Telefono","Direccion"};
        String [] registro = new String [5];
        Sucursal sucursal;
        ArrayList sucursales;
        tabla = new DefaultTableModel(null,titulos);
        sucursales = contol.listaSucursal ();
        for (int i = 0; i<sucursales.size(); i++)
        {
            sucursal = (Sucursal) sucursales.get(i);
            registro [0] = Integer.toString (sucursal.getSuc_codigo());
            registro [1] = Integer.toString (sucursal.getId_supervisor ());
            registro [2] = sucursal.getCorreo ();
            registro [3] = sucursal.getTelefono ();
            registro [4] = sucursal.getDireccion ();
            tabla.addRow (registro);
        }
        tablaSucursal.setModel(tabla);
        tablaSucursal.setEnabled(true);
    }
