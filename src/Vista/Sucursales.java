    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (operacion.equals("nuevo")){
           contol.registraSucursal(Integer.parseInt(idSupeTxt.getText()), correoTxt.getText(), telefonoTxt.getText(), direccionTxt.getText());
           idSupeTxt.setText("");
           direccionTxt.setText("");
           telefonoTxt.setText("");
           correoTxt.setText("");
        }
        else{
            if (operacion.equals("modifica")){
                contol.corrigeSucursal(Integer.valueOf(suc_codigo),Integer.parseInt(idSupeTxt.getText()), correoTxt.getText(), telefonoTxt.getText(), direccionTxt.getText());
            }
            cargaTabla();
        }
        cargaTabla();
}//GEN-LAST:event_GuardarActionPerformed

