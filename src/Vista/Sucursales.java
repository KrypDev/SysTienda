    private void tablaSucursalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaSucursalMouseClicked
        int fila = tablaSucursal.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Es necesario seleccionar un renglón");
        }
        else {
           operacion = "modifica";
           suc_codigo =(String)tabla.getValueAt(fila, 0);
           idSupeTxt.setText((String)tabla.getValueAt(fila, 1));
           correoTxt.setText((String)tabla.getValueAt(fila, 2));
           telefonoTxt.setText((String)tabla.getValueAt(fila, 3));
           direccionTxt.setText((String)tabla.getValueAt(fila, 4));
           Guardar.setEnabled(true);
           Cancelar.setEnabled(true);
           Borrar.setEnabled(true);
           Nuevo.setEnabled(false);
           direccionTxt.setEnabled(true);
           telefonoTxt.setEnabled(true);
           correoTxt.setEnabled(true);
           idSupeTxt.setEnabled(true);
        }
    }//GEN-LAST:event_tablaSucursalMouseClicked

