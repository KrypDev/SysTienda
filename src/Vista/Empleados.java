    private void cargaTabla (){
        String [] titulos = {"Id", "Sucursal", "Nombre", "Edad", "Género", "Dirección", "Telefono", "Referencias C."};
        String [] registro = new String [8];
        Empleado empleado;
        ArrayList empleados;
        tabla = new DefaultTableModel(null,titulos);
        empleados = contol.listaEmpleados ();
        for (int i = 0; i < empleados.size(); i++)
        {
            empleado = (Empleado) empleados.get(i);
            registro [0] = Integer.toString (empleado.getId_empleado ());
            registro [1] = Integer.toString (empleado.getSuc_codigo ());
            registro [2] = empleado.getNom_empleado ();
            registro [3] = Integer.toString (empleado.getEdad ());
            registro [4] = empleado.getGenero ();
            registro [5] = empleado.getDireccion ();
            registro [6] = empleado.getTel_empleado ();
            registro [7] = empleado.getReferencias_comerciales ();
            tabla.addRow (registro);
        }
        tablaEmpleados.setModel(tabla);
        tablaEmpleados.setEnabled(true);
    }

