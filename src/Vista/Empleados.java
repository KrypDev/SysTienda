    private void tablaEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpleadosMouseClicked
        int fila = tablaEmpleados.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Es necesario seleccionar un renglón");
        }
        else {
           operacion = "modifica";
           id =(String)tabla.getValueAt(fila, 0);
           idSucTxt.setText((String)tabla.getValueAt(fila, 1));
           nombreTxt.setText((String)tabla.getValueAt(fila, 2));
           edadTxt.setText((String)tabla.getValueAt(fila, 3));
           generoTxt.setText((String)tabla.getValueAt(fila, 4));
           direccionTxt.setText((String)tabla.getValueAt(fila, 5));
           telTxt.setText((String)tabla.getValueAt(fila, 6));
           refComTxt.setText((String)tabla.getValueAt(fila, 7));
           
           Guardar.setEnabled(true);
           Cancelar.setEnabled(true);
           Borrar.setEnabled(true);
           Nuevo.setEnabled(false);
           idSucTxt.setEnabled(true);
           nombreTxt.setEnabled(true);
           direccionTxt.setEnabled(true);
           telTxt.setEnabled(true);
           edadTxt.setEnabled(true);
           refComTxt.setEnabled(true);
           generoTxt.setEnabled(true);
        }
    }//GEN-LAST:event_tablaEmpleadosMouseClicked

