    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (operacion.equals("nuevo")){
           contol.registraEmpleado(Integer.parseInt(idSucTxt.getText()), nombreTxt.getText(), Integer.parseInt(edadTxt.getText()), generoTxt.getText(), direccionTxt.getText(), telTxt.getText(), refComTxt.getText());
           idSucTxt.setText("");
           nombreTxt.setText("");
           edadTxt.setText("");
           generoTxt.setText("");
           direccionTxt.setText("");
           telTxt.setText("");
           refComTxt.setText("");
        }
        else{
            if (operacion.equals("modifica")){
                contol.corrigeEmpleado(Integer.valueOf(id),Integer.parseInt(idSucTxt.getText()), nombreTxt.getText(), Integer.parseInt(edadTxt.getText()), generoTxt.getText(), direccionTxt.getText(), telTxt.getText(), refComTxt.getText());
            }
            cargaTabla();
        }
        cargaTabla();
}//GEN-LAST:event_GuardarActionPerformed

