    private void guardaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardaActionPerformed
        if (operacion.equals("nuevo")){
           contol.registraMercancia(nombreTxt.getText(), descripTxt.getText(), Integer.parseInt(precioTxt.getText()), Integer.parseInt(canExTxt.getText()),Integer.parseInt(sucTxt.getText()));
           nombreTxt.setText("");
           descripTxt.setText("");
           precioTxt.setText("");
           sucTxt.setText("");
           canExTxt.setText("");
        }
        else{
            if (operacion.equals("modifica")){
                contol.corrigeSucursal(Integer.valueOf(id),nombreTxt.getText(), descripTxt.getText(), Integer.parseInt(precioTxt.getText()), Integer.parseInt(canExTxt.getText()),Integer.parseInt(sucTxt.getText()));
            }
            cargaTabla();
        }
        cargaTabla();
}//GEN-LAST:event_guardaActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        String [] titulos = {"Id", "Nombre", "Descripcion","Precio","Cant. Existente", "Sucursal"};
        String [] registro = new String [5];
        Mercancia mercancia;
        ArrayList mercancias;
        tabla = new DefaultTableModel(null,titulos);
        mercancias = contol.listaMercanciaBuscados (buscarTxt.getText());
        for (int i = 0; i<mercancias.size(); i++)
        {
            mercancia = (Mercancia) mercancias.get(i);
            registro [0] = Integer.toString (mercancia.getMercancia_id());
            registro [1] = mercancia.getNom_mercancia ();
            registro [2] = mercancia.getDescripcion ();
            registro [3] = Integer.toString (mercancia.getPrecio ());
            registro [4] = Integer.toString (mercancia.getCantidad_ext ());
            registro [5] = Integer.toString (mercancia.getSuc_codigo ());
            tabla.addRow (registro);
        }
        tablaMercancia.setModel(tabla);
        tablaMercancia.setEnabled(true);
    }//GEN-LAST:event_buscarActionPerformed

