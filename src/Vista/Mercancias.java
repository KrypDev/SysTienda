    private void cargaTabla (){
        String [] titulos = {"Id", "Nombre", "Descripcion","Precio","Cant. Existente", "Sucursal"};
        String [] registro = new String [6];
        Mercancia mercancia;
        ArrayList mercancias;
        tabla = new DefaultTableModel(null,titulos);
        mercancias = contol.listaMercancia ();
        for (int i = 0; i<mercancias.size(); i++)
        {
            mercancia = (Mercancia) mercancias.get(i);
            registro [0] = Integer.toString (mercancia.getMercancia_id());
            registro [1] = mercancia.getNom_mercancia ();
            registro [2] = mercancia.getDescripcion ();
            registro [3] = Integer.toString (mercancia.getPrecio ());
            registro [4] = Integer.toString (mercancia.getCantidad_ext ());
            registro [5] = Integer.toString (mercancia.getSuc_codigo ());
            tabla.addRow (registro);
        }
        tablaMercancia.setModel(tabla);
        tablaMercancia.setEnabled(true);
    }
